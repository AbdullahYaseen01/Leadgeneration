# API shape: country code → state → city → postals
LOCATION_TREE: dict[str, dict[str, dict[str, list[str]]]] = {
    "CH": LOCATION_TREE_CH,
    "DE": LOCATION_TREE_DE,
}


def flatten_cities(tree: dict | None = None) -> list[str]:
    """Collect unique city names from a nested LOCATION_TREE."""
    t = tree if tree is not None else LOCATION_TREE
    acc: list[str] = []
    for country in t.values():
        for state, cities in country.items():
            if not isinstance(cities, dict):
                continue
            for city, postals in cities.items():
                if isinstance(postals, list) and len(postals) == 0:
                    acc.append(city)
                else:
                    acc.append(city)
    return sorted(_dedupe(acc))


CITIES: list[str] = flatten_cities(LOCATION_TREE)
